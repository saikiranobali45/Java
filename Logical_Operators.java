public class Logical_Operators {
    public static void main(String[] args) {
        int a=10;
        int b=200;
        int c=4;
        System.out.println("Logical Operators in Java");
        System.out.println(a>b && a<c);
        System.out.println(a==b || b!=c);
        System.out.println(!(b<a));
    }
}
