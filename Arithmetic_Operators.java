public class Arithmetic_Operators {
    public static void main(String[] args) {
        int a=10;
        int b=3;
        System.out.println("Arithmetic Operators");
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(--b);
        System.out.println(b--);
    }
}
