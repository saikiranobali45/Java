public class Relational_Operators {
    public static void main(String[] args) {
        int a=10;
        int b=9;
        System.out.println("Relational Operators in Java:");
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a!=b);
    }
}
